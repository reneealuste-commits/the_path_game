// Helper functions
export const getQuestById = (id) => QUESTS.find(q => q.id === id)

export const getQuestsByPhase = (phaseId) => QUESTS.filter(q => q.phase === phaseId)

export const getPhaseById = (id) => PHASES.find(p => p.id === id)

export const getTotalQuests = () => QUESTS.length

export const getPhaseProgress = (currentQuestId) => {
  const quest = getQuestById(currentQuestId)
  if (!quest) return { current: 0, total: 0 }
  const phaseQuests = getQuestsByPhase(quest.phase)
  const indexInPhase = phaseQuests.findIndex(q => q.id === currentQuestId) + 1
  return { current: indexInPhase, total: phaseQuests.length }
}
